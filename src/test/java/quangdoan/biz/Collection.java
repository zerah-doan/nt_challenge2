package quangdoan.biz;

public class Collection {
    private String id;
    private String title;

    public String getId() {
        return id;
    }

    public Collection setId(String id) {
        this.id = id;
        return this;
    }

    public String getTitle() {
        return title;
    }

    public Collection setTitle(String title) {
        this.title = title;
        return this;
    }
}
